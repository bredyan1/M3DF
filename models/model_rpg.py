def compute_optimal_transport(source, target, epsilon=0.01):
    """
    Compute Optimal Transport between source and target using Sinkhorn distance.

    Args:
        source (torch.Tensor): Source features of shape [batch_size, feature_dim].
        target (torch.Tensor): Target features of shape [batch_size, feature_dim].
        epsilon (float): Regularization parameter for Sinkhorn algorithm.

    Returns:
        torch.Tensor: Transported source features aligned to the target distribution.
    """
    # Flatten the tensors to [batch_size, feature_dim]
    source_flat = source.squeeze(1)  # Remove the extra dimension
    target_flat = target.squeeze(1)
    # Convert tensors to numpy arrays
    source_np = source_flat.detach().cpu().numpy()
    target_np = target_flat.detach().cpu().numpy()

    # Compute cost matrix (Euclidean distance)
    cost_matrix = ot.dist(source_np, target_np, metric='euclidean')

    # Uniform weights for source and target
    source_weights = np.ones(source_np.shape[0]) / source_np.shape[0]
    target_weights = np.ones(target_np.shape[0]) / target_np.shape[0]

    # Solve optimal transport problem using Sinkhorn algorithm
    transport_plan = ot.sinkhorn(source_weights, target_weights, cost_matrix, epsilon)

    # Align source to target
    transported_source = np.dot(transport_plan, target_np)

    # Convert back to torch tensor
    return torch.tensor(transported_source, device=source.device, dtype=source.dtype).unsqueeze(1)
