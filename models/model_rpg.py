class RPGCAT_Surv(Blip2Base):
    def __init__(self, fusion='concat', omic_sizes=[100, 200, 300, 400, 500, 600], n_classes=4,
                 model_size_wsi: str='small', model_size_omic: str='small',model_size_rad: str='small', dropout=0.25,num_query_token=48,
        cross_attention_freq=1,embed_dim=256):
        super(RPGCAT_Surv, self).__init__()
        self.fusion = fusion
        self.omic_sizes = omic_sizes
        self.n_classes = n_classes
        self.size_dict_WSI = {"small": [1024, 768, 256], "big": [1024, 512, 384]}
        self.size_dict_omic = {'small': [256, 768], 'big': [1024, 1024, 1024, 256]}
        self.size_dict_rad = {'small': [512, 768,256]}
        # self.size_dict_rad = {'small': [512, 768,256]}
        
        ### FC Layer over WSI bag,这里输入的都是由CLAM提取的[M,1024]的特征向量。
        size = self.size_dict_WSI[model_size_wsi]
        fc = [nn.Linear(size[0], size[1]), nn.ReLU()]
        fc.append(nn.Dropout(0.25))
        self.wsi_net = nn.Sequential(*fc)

        ### FC Layer over rad,这里输入的都是提取的[1,1024]的特征向量。
        size_rad = self.size_dict_rad[model_size_rad]
        fc = [nn.Linear(size_rad[0], size_rad[1]), nn.ReLU()]
        fc.append(nn.Dropout(0.25))
        self.rad_net = nn.Sequential(*fc)

        ### Constructing Genomic SNN
        hidden = self.size_dict_omic[model_size_omic]
        sig_networks = []
        for input_dim in omic_sizes:
            fc_omic = [SNN_Block(dim1=input_dim, dim2=hidden[0])]
            for i, _ in enumerate(hidden[1:]):
                fc_omic.append(SNN_Block(dim1=hidden[i], dim2=hidden[i+1], dropout=0.25))
            sig_networks.append(nn.Sequential(*fc_omic))
        self.sig_networks = nn.ModuleList(sig_networks)

        ### Multihead Attention
        self.coattn = MultiheadAttention(embed_dim=256, num_heads=1)

        ### Path Transformer + Attention Head
        path_encoder_layer = nn.TransformerEncoderLayer(d_model=256, nhead=8, dim_feedforward=512, dropout=dropout, activation='relu')
        self.path_transformer = nn.TransformerEncoder(path_encoder_layer, num_layers=2)
        self.path_attention_head = Attn_Net_Gated(L=size[2], D=size[2], dropout=dropout, n_classes=1)
        self.path_rho = nn.Sequential(*[nn.Linear(size[2], size[2]), nn.ReLU(), nn.Dropout(dropout)])

        ### rad Transformer + Attention Head
        rad_encoder_layer = nn.TransformerEncoderLayer(d_model=256, nhead=8, dim_feedforward=512, dropout=dropout, activation='relu')
        self.rad_transformer = nn.TransformerEncoder(rad_encoder_layer, num_layers=2)
        self.rad_attention_head = Attn_Net_Gated(L=size_rad[2], D=size_rad[2], dropout=dropout, n_classes=1)
        self.rad_rho = nn.Sequential(*[nn.Linear(size_rad[2], size_rad[2]), nn.ReLU(), nn.Dropout(dropout)])
        
        ### Omic Transformer + Attention Head
        omic_encoder_layer = nn.TransformerEncoderLayer(d_model=256, nhead=8, dim_feedforward=512, dropout=dropout, activation='relu')
        self.omic_transformer = nn.TransformerEncoder(omic_encoder_layer, num_layers=2)
        self.omic_attention_head = Attn_Net_Gated(L=size[2], D=size[2], dropout=dropout, n_classes=1)
        self.omic_rho = nn.Sequential(*[nn.Linear(size[2], size[2]), nn.ReLU(), nn.Dropout(dropout)])
        self.gate = nn.Sequential(
            nn.Linear(256*3, 256),  # 三模态权重
            nn.ReLU(),
            nn.Dropout(p=0.25),  # 增加 Dropout
            nn.Linear(256, 3),  # 输出三模态权重
            nn.Softmax(dim=-1)
        )

        self.Qformer, self.query_tokens = self.init_Qformer(
            num_query_token, _ , cross_attention_freq
        )
        self.Qformer1, self.query_tokens1 = self.init_Qformer(
            num_query_token, _ , cross_attention_freq
        )

        self.vision_proj = nn.Linear(self.Qformer.config.hidden_size, embed_dim)
        self.text_proj = nn.Linear(self.Qformer.config.hidden_size, embed_dim)
        self.vision_proj1 = nn.Linear(self.Qformer1.config.hidden_size, embed_dim)
        self.text_proj1 = nn.Linear(self.Qformer1.config.hidden_size, embed_dim)
        self.itm_head = nn.Linear(self.Qformer.config.hidden_size, 2)
        self.itm_head1 = nn.Linear(self.Qformer1.config.hidden_size, 2)
        self.temp = nn.Parameter(0.07 * torch.ones([]))
        self.temp1 = nn.Parameter(0.07 * torch.ones([]))
        # for name, param in self.Qformer.named_parameters():
        #     param.requires_grad = False  # 完全冻结
        #     if "crossattention" in name:  # 仅解冻交叉注意力层
