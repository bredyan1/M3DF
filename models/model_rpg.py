        g_h_coattn, A_path_rad_coattn = self.coattn(h_omic_bag, path_gene_feats, path_gene_feats)




        # h_r_coattn, A_path_gene_coattn = self.coattn(r_h_coattn, h_rad_bag, h_rad_bag)

        # h_g_coattn, A_path_rad_coattn = self.coattn(r_h_coattn, h_omic_bag, h_omic_bag)
